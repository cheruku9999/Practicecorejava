package Superkeyworddemo;

public class A {

		public A()
	{
		System.out.println("parent class constructor");
		
	}
		public A(int i)//parameterized constructor
	
		{
			System.out.println("parent class contructor with  value of i" + i);
			
		}
		
		public A(int i, int j)//parameterized constructor
		
		{
			System.out.println("parent class contructor with  value of i" + i);
			System.out.println("parent class contructor with  value of j" + j);
			
		}
}
